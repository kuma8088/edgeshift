/**
 * Checks if an email address uses a disposable/temporary email domain.
 *
 * @param email - The email address to check
 * @returns true if the email uses a known disposable domain, false otherwise
 *
 * @example
 * ```typescript
 * isDisposableEmail('user@mailinator.com'); // true
 * isDisposableEmail('user@gmail.com');      // false
 * isDisposableEmail('USER@MAILINATOR.COM'); // true (case-insensitive)
 * isDisposableEmail('invalid-email');       // false (invalid format)
 * ```
 */
export function isDisposableEmail(email: string): boolean {
  // Extract domain from email
  const parts = email.split('@');

  // Invalid email format: missing @, multiple @, empty local part, or empty domain
  if (parts.length !== 2 || !parts[0] || !parts[1]) {
    return false;
  }

  const domain = parts[1].toLowerCase();

  // Check if domain is in disposable list (O(1) lookup with Set)
  return DISPOSABLE_DOMAINS.has(domain);
}

/**
 * Returns the number of disposable domains in the database.
 * Useful for monitoring and testing.
 *
 * @returns The total count of known disposable domains
 */
export function getDisposableDomainCount(): number {
  return DISPOSABLE_DOMAINS.size;
}
